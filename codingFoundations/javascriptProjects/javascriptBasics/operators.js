// Comparaison operators

//Equal to:
console.log('3' == 3); //True JS uses coercion when comparing values. Coercion coverts a value from one type to another

// Strict equal to:
console.log('3' === 3); //False compares the datatype and value of the values in question.

//Not equal to:
console.log('3' != 3); //False compares using coecrion, values are equal in this example

//Strict not equal to:
console.log('3' !== 3); //True Compares the datatype and the values to see if they are equal. these values are not equal in this example.

//Greater and less than:
console.log(3 > 2);
console.log(2 < 3);

//Greater and less than or equal to:
console.log(3 >= 2);
console.log(2 <= 3);

//And operator
console.log(2 && 3);

// Or operator
console.log(2 || 3);

